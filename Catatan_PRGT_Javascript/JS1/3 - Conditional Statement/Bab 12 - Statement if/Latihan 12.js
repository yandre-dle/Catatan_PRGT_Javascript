/*
Mari kita gunakan statement "if" pada latihan ini!
Constant "level" Print pesan sesuai dengan nilai "level"
*/
const level = 12;

/*
Buat statement if dengan kondisi "level > 10"
*/
// Tambahkan pernyataan if dengan kondisi: level > 10
if (level > 10) {
    /*
    Di statement if, 
    gunakan console.log 
    untuk mem-print string "Level Anda lebih tinggi dari 10"
    */
    console.log("Level Anda lebih tinggi dari 10");
}
