/*
menggunakan && untuk membuat statement if dengan beberapa kondisi
*/

const age = 24;

/*
Tambahkan statement "if"
dengan kondisi untuk jika constant "age" sama dengan atau lebih besar dari 20 dan kurang dari 30".
Di statement "if", cetak string "Saya di usia 20-an tahun"
*/
// Tambahkan pernyataan if dengan kondisi yang telah ditentukan
if (age >= 20 && age < 30) {
  console.log("Saya di usia 20-an tahun");
}
