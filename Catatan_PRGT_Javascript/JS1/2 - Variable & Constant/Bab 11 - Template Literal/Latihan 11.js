/*
menggunakan template literal untuk memasukkan nilai constant di dalam string.
Constant "name" dan "age" 
*/
const name = "Ninja Ken";
const age = 14;

/*
Gunakan template literal untuk mencetak "Nama saya adalah ____" di console
Bidang kosong harus diganti dengan nilai constant "name"
*/
// Cetak string "Nama saya adalah ____"
console.log(`Nama saya adalah ${name}`);

/*
Gunakan template literal untuk mencetak "Hari ini saya berusia ____ tahun" di console.
Bidang kosong harus diganti dengan nilai constant "age"
*/
// Cetak string "Hari ini saya berusia ____ tahun"
console.log(`Hari ini saya berusia ${age} tahun`);
