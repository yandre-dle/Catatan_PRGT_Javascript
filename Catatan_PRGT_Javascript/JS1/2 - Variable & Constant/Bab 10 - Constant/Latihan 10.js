/*
Deklarasikan constant yang disebut "language" 
dan berikan nilai stringnya, yaitu "Prancis"
*/
// Deklarasikan constant language
const language = "Prancis";

/*
Cetak nilai constant language di console.
*/
// Cetak nilai constant language
console.log(language);

/*
Untuk variable, constant dapat digabungkan dengan string.
Menggunakan constant "language", 
cetak kalimat "Saya bisa berbicara bahasa ____" di console,
namun sisipkan nilai constant ke bidang kosong "Saya bisa berbicara bahasa"
*/
// Gunakan constant language untuk mencetak "Saya bisa berbicara bahasa ____"
console.log("Saya bisa berbicara bahasa " + language);
